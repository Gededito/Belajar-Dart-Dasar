/*
    Operator Perbandingan
    - Operator perbandingan adalah operator yang hasilnya adalah boolean
    - Operator perbandingan bisa digunakan di tipe data number dan string

    Operator Perbandingan :
    1. == : Sama dengan
    2. !=  : Tidak sama dengan
    3. >  : Lebih dari
    4. <  : Kurang dari
    5. >=  : Lebih dari atau sama dengan
    6. <=  : Kurang dari atau sama dengan

*/

void main() {

  print(10 == 10); // true
  print(10 != 10); // false
  print(10 > 10); // false
  print(10 < 10); // false
  print(10 >= 10); // true
  print(10 <= 10); // true

  // Membandingkan String
  print("dito" == "dito"); // true

}