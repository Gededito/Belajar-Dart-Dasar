/*

    Function Short Expression
    - Dart mendukung function short expression
    - Dimana jika terdapat sebuah function yang hanya satu baris, Kita bisa
          menyingkatnya secara sederhana
    - Untuk membuat function short expression, Kita tidak butuh kurung { } dan
          juga tidak butuh kata kunci return

*/

// Membuat Function Short Expression
int sum(int first, int second) => first + second;

void main() {

  print(sum(10,10));

}
