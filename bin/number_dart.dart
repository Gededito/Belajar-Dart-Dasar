/*

    Number :
    - Number adalah tipe data angka, terdapat dua jenis tipe data Number,
          int dan double
    - int adalah tipe data bilangan bulat
    - double adalah tipe data bilangan desimal
    - Penulisan koma dalam double menggunakan titik, bukan koma. Jadi jika kita
          akan membuat bilangan desimal 0,5 (nol koma lima), maka ditulis 0.5

    num :
    - Jika kita ingin menggunakan tipe data number yang bisa int ataupun double,
          kita bisa menggunakan tipe data num



*/

void main() {

  int number1 = 10;
  double number2 = 10.65;

  /*
      Jika kita menggunakan tipe data num, Maka kita bisa mengubah int ke double
        atau sebaliknya
  */
  num number = 10.5;
  print(number);

  number = 10;
  print(number);

  print(number1);
  print(number2);

}
