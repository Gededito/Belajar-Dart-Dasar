/*

    While Loop
    - While Loop adalah versi perulangan yang lebih sederhana dibanding for loop
    - Di While Loop, hanya terdapat kondisi perulangan, tanpa ada init statement
          dan post statement

*/

void main() {

  var counter = 1;

  while (counter <= 4) {
    print("Cetakan ke-$counter");
    counter++;
  }

}