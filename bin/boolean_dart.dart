/*

    Boolean :
    - Boolean adalah tipe data yang hanya memiliki dua nilai, yaitu benar dan
          salah
    - Boolean direpresentasikan dengan kata kunci bool
    - Untuk nilai benar, menggunakan kata kunci true
    - Untuk nilai salah, menggunakan kata kunci false

*/

void main() {

  bool finish = false;
  print(finish);

  finish = true;
  print(finish);

}